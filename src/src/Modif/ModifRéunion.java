    public void initList(){
        Bdd bdd = new Bdd();
        liste_types = bdd.allTypes();
        
        if(liste_types.size()==1){
            Type.addItem(liste_types.get(0).getType());
        }
        
        else if(liste_types.size()>1){
            for(int i=0;i<liste_types.size();++i){
                Type.addItem(liste_types.get(i).getType());
            }
        }
       
        
        
    }
