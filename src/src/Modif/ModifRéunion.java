    private void BEnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEnregisterActionPerformed

        Bdd bdd = new Bdd();

        String r_nom=Nom.getText();
        String r_lieu=Lieu.getText();

        String r_date=Date.getText();

        String r_heure=Heure.getText();

        int r_typereu = Type.getSelectedIndex()+1;
        
        
        System.out.println("type choisi sur bouton ajout" + r_typereu);

        int r_perio = 0;
        int r_duree = 0;

        if(Periodicite.isSelected())
        {
            r_perio=1;
            r_duree=0; //Remplacer par nombre de semaines selectionnées
        }
        else {
            r_perio=0;
            r_duree=0;
        }

        bdd.Modifier_reunion(id_reu,r_typereu,r_nom,r_lieu,r_date,r_heure,r_perio,r_duree);
        
        this.dispose();
    }//GEN-LAST:event_BEnregisterActionPerformed
    

    public void Affichage(int id_reu,String nom_reu,int type_reu,String lieu_reu,String date_reu,String heure_reu,boolean perio_reu,int semaines_reu)
    {
        setIdReu(id_reu); //pour savoir quelle réu pour pouvoir la modifier 
        
        Nom.setText(nom_reu);
        Lieu.setText(lieu_reu);
        Date.setText(date_reu);
        Heure.setText(heure_reu);
        System.out.println("type chosii a l'init " + type_reu);
        Type.getModel().setSelectedItem(type_reu); //Problème au niveau de la réu selectionné 
        Periodicite.setSelected(perio_reu);
        nbjours.setText(Integer.toString(semaines_reu));
        
        
    }
