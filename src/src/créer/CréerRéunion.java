    public void ajout_bdd(String r_nom,String r_typereu,String r_date,String r_heure,String r_lieu,int r_perio,int r_duree){
        Bdd bdd = new Bdd();
        ArrayList<Types> liste = bdd.allTypes();
                   
        int id_type=0;
        for (int i=0;i<liste.size();i++){
            if (r_typereu.equals(liste.get(i).getType())){
               id_type = liste.get(i).getId();
               break;
            }
                           //get l'id de la réunion 
        }
        bdd.ajouterReunion(r_nom,r_date,r_heure, r_lieu, r_perio, r_duree, id_type);
        
        bdd.add_personne_newreu(); //prend la liste temp personne et l'ajoute à la réu
        
        bdd.add_groupe_newreu(); //prend la liste temp du groupe et l'ajoute à la réu
        

        
        
    }
    
    
    public void Types_init(){
        Bdd bdd = new Bdd();
        
         ArrayList<Types> liste = bdd.allTypes();
        if (liste.isEmpty() == false){
        for (int i=0;i<liste.size();++i)
        {
            Type.addItem(liste.get(i).getType());
        }
        }
    }
    
