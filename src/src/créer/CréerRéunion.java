    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
     
        String r_nom=Nom.getText();
        
        if(r_nom.equals("")){
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame,"le nom de la réunion ne peut pas être vide ! ");
        }
        else{
            String r_lieu=Lieu.getText();
        
            String r_date=Date.getText();

            String r_heure=Heure.getText();

            String r_typereu = (String)Type.getSelectedItem();
            
            DateTimeFormatter dtf;
            dtf = DateTimeFormatter.ofPattern("dd-MM-yyyy");
            LocalDate current_date = LocalDate.now();
            System.out.println("date ajd " + current_date);

            int r_perio=0;
            int r_duree=0;
            if(Periodicite.isSelected())
            {
                r_perio=1;
                r_duree=0; //Remplacer par nombre de semaines selectionnées 
            }
            else {
                r_perio=0;
                r_duree=0;
            }


            if(!r_date.equals("")){ 

                    //appel de la fonction
                    LocalDate d_date_reu = LocalDate.parse(r_date, dtf);
                    long daysBetween = 0;
                    daysBetween = ChronoUnit.DAYS.between(current_date, d_date_reu);

                    if(daysBetween<21){
                            JFrame frame = new JFrame();
                            JOptionPane.showMessageDialog(frame,"Cette réunion ne peut pas être ajouté car la date est inférieure à 21 jours.");
                    }
                    else{

                        if(!r_heure.equals("")){//on vérifie qu'on a pas une réunion 1 heure avant 
                            System.out.println("on vérifie l'heure");
                            Bdd bdd = new Bdd();
                            boolean verif_heure = bdd.verif_heure(r_date,r_heure);

                            if(verif_heure==true){
                                 ajout_bdd(r_nom,r_typereu,r_date,r_heure,r_lieu,r_perio,r_duree);
                                 System.out.println("taille de la liste :" + liste_pers.size());
                            }

                            else{
                                JFrame frame = new JFrame();
                                JOptionPane.showMessageDialog(frame,"Vous avez déjà une réunion programmé à moins d'une heure.");
                            }
                        }
                        else{
                            ajout_bdd(r_nom,r_typereu,r_date,r_heure,r_lieu,r_perio,r_duree);
                            
                            //ajout des personnes et du groupe

                        }



                    }

                }//ferme le grand if
                else{
                    ajout_bdd(r_nom, r_typereu, r_date, r_heure, r_lieu, r_perio, r_duree);
                    
                    //ajout des personnes et du groupe
                    

                }

        
        this.dispose();
        }
        
                
        
    }//GEN-LAST:event_btn_saveActionPerformed

